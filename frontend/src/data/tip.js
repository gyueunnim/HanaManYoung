export function getTodaysTips() {
  const today = new Date();

  const year = today.getFullYear();
  const month = today.getMonth() + 1; // 월은 0부터 시작하므로 +1
  const day = today.getDate();

  const dateString = `${year}-${month}-${day}`;

  let hash = 0;
  for (let i = 0; i < dateString.length; i++) {
    hash = dateString.charCodeAt(i) + ((hash << 5) - hash);
  }

  const firstIndex = Math.abs(hash) % tips.length;

  const secondIndex = (firstIndex + 7) % tips.length; // 7을 더해 다른 인덱스 생성

  // 첫 번째와 두 번째 인덱스가 동일하면 다른 방식으로 두 번째 인덱스를 생성
  if (firstIndex === secondIndex) {
    const secondHash = (hash + 13) % tips.length;
    return [tips[firstIndex], tips[secondHash]];
  }

  return [tips[firstIndex], tips[secondIndex]];
}
